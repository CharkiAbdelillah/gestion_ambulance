    private void btnConnecterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnecterActionPerformed

        String host=textHost.getText();
        int port=Integer.parseInt(textPort.getText());
        try{
            Socket socket=new Socket(host,port);
            InputStream inputStream=socket.getInputStream();
            InputStreamReader isr=new InputStreamReader(inputStream);
            BufferedReader bufferedReader=new BufferedReader(isr);
            pw=new PrintWriter(socket.getOutputStream(),true);
            new Thread(()->{
                try{
                    while (true) {//a chaque fois si je connecte va lancer une thread, odik thread tay tsanat wash kayna xi reponse
                        String reponse=bufferedReader.readLine();
                        list1.add(reponse);
                        //                                listModel.add(reponse);//hadi tat afficher f interface javafx odeja javafx tat khdm b thread o ila drna thread mn 3nda ghat w93 exception dik xi lax drna had runlater
                    }
                }catch(IOException o){
                    o.printStackTrace();
                }
            }).start();
        }catch(IOException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnConnecterActionPerformed

